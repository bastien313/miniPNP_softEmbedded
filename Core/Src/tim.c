/*
 * Delais us, max = 6500uS
 */
void delay_us(unsigned short delay)
{
	TIM7->CNT = 0;
	TIM7->PSC = 16;
	TIM7->ARR = delay * 10;
	TIM7->EGR |= TIM_EGR_UG;
	TIM7->SR = 0x0000;
	TIM7->CR1 |= TIM_CR1_CEN;

	while(!(TIM7->SR & TIM_SR_UIF));
}

